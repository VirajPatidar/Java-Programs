        String s1 = jTextField1.getText();
        int rno = Integer.parseInt(jTextField2.getText());
        String gender="Male";
        String Dept="";
        String hobbies="";
        if(jRadioButton2.isSelected()){
            gender="Female";
        }
        Dept=jComboBox1.getSelectedItem().toString();
        
        if(jCheckBox1.isSelected())
            hobbies+=jCheckBox1.getText()+" ";
        if(jCheckBox2.isSelected())
            hobbies+=jCheckBox2.getText()+" ";
        if(jCheckBox3.isSelected())
            hobbies+=jCheckBox3.getText()+" ";

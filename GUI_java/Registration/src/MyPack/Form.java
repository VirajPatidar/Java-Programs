    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int rno = Integer.parseInt(jTextField2.getText());
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/registration?zeroDateTimeBehavior=CONVERT_TO_NULL","root","");
            PreparedStatement ps = (PreparedStatement) con.prepareStatement("SELECT * FROM `info` WHERE `Roll Number`="+rno);
            ResultSet rs = (ResultSet) ps.executeQuery();
            String res="";
            while(rs.next()){
                res+="Roll No:"+(String.valueOf(rs.getInt(1)))+"\n"+"Name:"+rs.getString(2)+"\n"+"Gender:"+rs.getString(3)+"\n"+"Department:"+rs.getString(4)+"\n"+"Hobbies:"+rs.getString(5);
            }
            JOptionPane.showMessageDialog(this,res);
            con.close();
        }catch(Exception e){JOptionPane.showMessageDialog(this, "Error" + e );}  
    }//GEN-LAST:event_jButton3ActionPerformed

